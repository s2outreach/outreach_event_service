package com.cts.outreach.event.entity;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBRangeKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

@DynamoDBTable(tableName="event")
public class EventEntity {
	
	private String eventid;
	private String eventname;
	private String eventdate;
	private String reqdCount;
	
	@DynamoDBHashKey(attributeName="eventid")
	@DynamoDBAutoGeneratedKey
	public String getEventid() {
		return eventid;
	}
	public void setEventid(String eventid) {
		this.eventid = eventid;
	}
	
	@DynamoDBAttribute
	public String getEventname() {
		return eventname;
	}
	public void setEventname(String eventname) {
		this.eventname = eventname;
	}
	
	@DynamoDBRangeKey(attributeName="eventdate")
	public String getEventdate() {
		return eventdate;
	}
	public void setEventdate(String eventdate) {
		this.eventdate = eventdate;
	}
	
	@DynamoDBAttribute
	public String getReqdCount() {
		return reqdCount;
	}
	public void setReqdCount(String reqdCount) {
		this.reqdCount = reqdCount;
	}
	
	public EventEntity(String eventid, String eventname, String eventdate, String reqdCount) {
		super();
		this.eventid = eventid;
		this.eventname = eventname;
		this.eventdate = eventdate;
		this.reqdCount = reqdCount;
	}	

}

